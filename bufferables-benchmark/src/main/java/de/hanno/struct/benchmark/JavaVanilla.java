package de.hanno.struct.benchmark;

public class JavaVanilla {
    private final int a;
    private final float b;
    private final long c;

    public JavaVanilla(int a, float b, long c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public float getB() {
        return b;
    }

    public long getC() {
        return c;
    }

}
